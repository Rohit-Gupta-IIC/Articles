import matplotlib.pyplot as plt   
    
# x axis values   
x = [1,2,3]   
# corresponding y axis values   
y = [3,1,2]   
    
# plotting the points    
plt.plot(x, y)   
    
# naming the x axis   
plt.xlabel('x - axis')   
# naming the y axis   
plt.ylabel('y - axis')   
    
# giving a title to my graph   
plt.title('2D Line Plot!')   
    
# function to show the plot   
plt.show()  
